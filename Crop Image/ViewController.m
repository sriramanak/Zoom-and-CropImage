- (UIImage*)cropImageFromScroll:(UIScrollView *)scrollview
{
    
    CGSize pageSize = scrollview.frame.size;
    UIGraphicsBeginImageContext(pageSize);
    CGContextRef resizedContext = UIGraphicsGetCurrentContext();
    int offsetX = -1*scrollview.contentOffset.x;
    int offsetY = -1*scrollview.contentOffset.y;
    CGContextTranslateCTM(resizedContext, offsetX, offsetY);
    [self.scrollView.layer renderInContext:resizedContext];
    UIImage *viewImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    UIImageWriteToSavedPhotosAlbum(viewImage, nil, nil, nil);
    
    return viewImage;
}


- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    // Make sure your segue name in storyboard is the same as this line
    if ([[segue identifier] isEqualToString:@"crop"])
    {
        // Get reference to the destination view controller
        UIImage *viewImage=[self cropImageFromScroll:self.scrollView];
        ShowImageViewController  *vc = [segue destinationViewController];
        vc.img=viewImage;
        vc.scroll=self.scrollView;
    }
}


@end
